        ตกลงที่จะร่วมมือด้านการวิจัยและพัฒนาอัญมณีและเครื่องประดับ เพื่อความเป็นเลิศ ทางวิชาการและเพื่อพัฒนาและส่งเสริมการส่งออกแก่อุตสาหกรรมอัญมณีและเครื่องประดับ โดยคณะ
        วิทยาศาสตร์ตกลงให้ใช้พื้นที่ส่วนหนึ่งในอาคารวิจัยและตรวจสอบอัญมณี อุปกรณ์และอนุญาตให้อาจารย์ให้ ความร่วมมือในด้านการวิจัยและตรวจสอบอัญมณี เป็นเวลา 10 ปี (9 กันยายน 2541 – 9
        กันยายน 2551) ต่อมา เมื่อวันที่ 1 ตุลาคม 2541 ที่ประชุมคณะกรรมการสถาบันสถาบันซึ่งประกอบด้วยภาครัฐและเอกชน จำนวน 28 ท่าน โดยมีนายสมพล เกียรติไพบูลย์ เป็นประธาน ได้มีมติแต่งตั้ง
        ศาสตราจารย์ศักดา ศิริพันธุ์ เป็นผู้อำนวยการสถาบัน
    </p>
    <p>
        จากนั้นเมื่อพระราชบัญญัติองค์การมหาชน พ.ศ.2542 ประกาศในราชกิจจานุเบกษา เมื่อวันที่ 24 กุมภาพันธ์ 2542 สถาบันจึงได้ดำเนินการยกร่างพระราชกฤษฎีกาจัดตั้งสถาบันวิจัยและพัฒนา
        อัญมณีและเครื่องประดับแห่งชาติ (องค์การมหาชน) ตามแนวทางและขั้นตอนในการดำเนินการจัดตั้งเป็นองค์การมหาชนตามที่สำนักงานคณะกรรมการปฏิรูประบบราชการ สำนักงาน ก.พ. กำหนด และ
        เมื่อวันที่ 3 มิถุนายน พ.ศ.2546 คณะรัฐมนตรีได้มีมติอนุมัติหลักการ ร่างพระราชกฤษฎีกาฯ จนในวันที่ 31 ธันวาคม พ.ศ.2546 จึงได้มีการประกาศ “พระราชกฤษฎีกาจัดตั้งสถาบันวิจัยและ พัฒนา
        อัญมณีและเครื่องประดับแห่งชาติ (องค์การมหาชน) พ.ศ.2546” ในราชกิจจานุเบกษา ฉบับกฤษฎีกา เล่ม 120 ตอนที่ 130ก สถาบันมีชื่อโดยย่อว่า สวอ. และมีชื่อเป็นภาษาอังกฤษว่า The Gem and
        Jewelry Institute of Thailand (Public Organization) (GIT) มีวัตถุประสงค์หลักเพื่อส่งเสริมและสนับสนุนอุตสาหกรรมอัญมณีและเครื่องประดับไทย เพื่อเพิ่มศักยภาพในการแข่งขันด้านการค้า
        ทั้งในประเทศและต่างประเทศ สถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ(องค์การมหาชน) บริหารงานและควบคุมดูแลโดย “คณะกรรมการบริหารสถาบันวิจัยและพัฒนาอัญมณีและ
        เครื่องประดับแห่งชาติ” มีจำนวน 11 คน ตามพระราชกฤษฎีกาจัดตั้งสถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ(องค์การมหาชน) พ.ศ. 2546 มาตรา 15 ประกอบด้วย ประธานกรรมการ
        (แต่งตั้งโดยคณะรัฐมนตรี) กรรมการโดยตำแหน่ง 4 คน ได้แก่ ผู้แทนกระทรวงทรัพยากรธรรมชาติและสิ่งแวดล้อม ผู้แทนกระทรวงพาณิชย์ ผู้แทนกระทรวงอุตสาหกรรม และผู้แทนสำนักงบประมาณ
        กรรมการผู้ทรงคุณวุฒิไม่เกิน 5 คน (แต่งตั้งโดยคณะรัฐมนตรี) และมีผู้อำนวยการสถาบันเป็นกรรมการและเลขานุการ
    </p>
    <p>
        ต่อมา พระราชกฤษฎีกาจัดตั้งสถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ(องค์การมหาชน) (ฉบับที่ 2) พ.ศ. 2561 มาตรา 5 ให้ยกเลิกความในมาตรา 15 แห่งพระราชกฤษฎีกาจัดตั้ง
        สถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ พ.ศ. 2546 และให้มีคณะกรรมการคณะหนึ่งเรียกว่า “คณะกรรมการสถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ” มีจำนวน 11
        คน ประกอบด้วย ประธานกรรมการ (แต่งตั้งโดยคณะรัฐมนตรี) กรรมการโดยตำแหน่ง 3 คน ได้แก่ ผู้แทนกระทรวงทรัพยากรธรรมชาติและสิ่งแวดล้อม ผู้แทนกระทรวงพาณิชย์ และผู้แทนกระทรวง
        อุตสาหกรรม กรรมการผู้ทรงคุณวุฒิไม่เกิน 6 คน (แต่งตั้งโดยคณะรัฐมนตรี) และให้ผู้อำนวยการสถาบันเป็นกรรมการและเลขานุการโดยตำแหน่ง คณะกรรมการสถาบันมีอำนาจหน้าที่ควบคุมดูแลโดย
        ทั่วไปซึ่งกิจการและการดำเนินการของสถาบัน เพื่อให้เป็นไปตามวัตถุประสงค์ที่กำหนดไว้ รวมถึงกำหนดนโยบายการบริหารงาน และให้ความเห็นชอบแผนการดำเนินงานของสถาบัน อนุมัติงบประมาณ
        ประจำปี งบการเงิน แผนการลงทุน และแผนการเงินของสถาบัน รวมทั้งออกระเบียบ ข้อบังคับ ข้อกำหนด หรือประกาศที่เกี่ยวกับสถาบัน
    </p>
    <p>
        คณะกรรมการสถาบันสถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ ในการประชุมครั้งที่ 2/2548 เมื่อวันที่ 14 กุมภาพันธ์ 2548 มีมติแต่งตั้งนางวิลาวัณย์ อติชาติ
        เป็นผู้อำนวยการสถาบัน ตั้งแต่วันที่ 1 มีนาคม 2548 เป็นต้นไป
    </p>
    <p>
        คณะกรรมการสถาบันสถาบันวิจัยและพัฒนาอัญมณีและเครื่องประดับแห่งชาติ ในการประชุมครั้งที่ 3/2556 เมื่อวันที่ 20 มีนาคม 2556 มีมติแต่งตั้ง นางพรสวาท วัฒนกูล
        เป็นผู้อำนวยการสถาบัน ตั้งแต่วันที่ 1 พฤษภาคม 2556 เป็นต้นไป
