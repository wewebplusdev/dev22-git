                            ตกลงที่จะร่วมมือด้านการวิจัยและพัฒนาอัญมณีและเครื่องประดับ เพื่อความเป็นเลิศ ทางวิชาการและเพื่อพัฒนาและส่งเสริมการส่งออกแก่อุตสาหกรรมอัญมณีและเครื่องประดับ โดยคณะ
                            วิทยาศาสตร์ตกลงให้ใช้พื้นที่ส่วนหนึ่งในอาคารวิจัยและตรวจสอบอัญมณี อุปกรณ์และอนุญาตให้อาจารย์ให้ ความร่วมมือในด้านการวิจัยและตรวจสอบอัญมณี เป็นเวลา 10 ปี (9 กันยายน 2541 – 9
