                            อัญมณีและเครื่องประดับแห่งชาติ (องค์การมหาชน) ตามแนวทางและขั้นตอนในการดำเนินการจัดตั้งเป็นองค์การมหาชนตามที่สำนักงานคณะกรรมการปฏิรูประบบราชการ สำนักงาน ก.พ. กำหนด และ
